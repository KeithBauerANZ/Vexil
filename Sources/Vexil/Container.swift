import Foundation

/// A `FlagContainer` is a type that encapsulates your `Flag` and `FlagGroup`
/// types. The only requirement of a `FlagContainer` is that it can be initialised
/// with an empty `init()`.
///
public protocol FlagContainer {
    init()
}
